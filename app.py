def get_precise_panchang_v2(date_obj, lat_val, lon_val):
    lat, lon = float(lat_val), float(lon_val)
    y, m, d = int(date_obj.year), int(date_obj.month), int(date_obj.day)
    
    # 0.0 UT = 5:30 AM IST
    jd_ut = swe.julday(y, m, d, 0.0)
    swe.set_sid_mode(swe.SIDM_LAHIRI)
    swe.set_topo(lon, lat, 0.0)

    def get_raw_astro(jd):
        # роиро┐ро▓ро╡рпБ рооро▒рпНро▒рпБроорпН роЪрпВро░ро┐роп рокро╛роХрпИ
        m_pos, _ = swe.calc_ut(jd, 1, int(swe.FLG_SIDEREAL))
        s_pos, _ = swe.calc_ut(jd, 0, int(swe.FLG_SIDEREAL))
        m_deg, s_deg = m_pos[0], s_pos[0]
        
        diff = (m_deg - s_deg) % 360
        t_idx = int(diff / 12)  # родро┐родро┐
        n_idx = int(m_deg / (360/27)) # роироЯрпНроЪродрпНродро┐ро░роорпН
        y_idx = int(((m_deg + s_deg) % 360) / (360/27)) # ропрпЛроХроорпН
        k_idx = int(diff / 6) % 11 # роХро░рогроорпН
        
        return m_deg, s_deg, t_idx, n_idx, y_idx, k_idx

    # --- роЙроЩрпНроХро│ро┐ройрпН роЕроирпНрод 35-Iteration родрпБро▓рпНро▓ро┐ропрооро╛рой рокро╛роХрпИ роХрогроХрпНроХрпАроЯрпБ ---
    def find_end_precise(jd_base, current_idx, c_type):
        low, high = 0.0, 1.3
        for _ in range(35):
            mid = (low + high) / 2
            m, s, t, n, y, k = get_raw_astro(jd_base + mid)
            val = n if c_type == "nak" else t
            if val == current_idx: low = mid
            else: high = mid
        return datetime.combine(date_obj, datetime.min.time()) + timedelta(hours=5.5) + timedelta(days=low)

    m_start, s_start, t_now, n_now, y_now, k_now = get_raw_astro(jd_ut)
